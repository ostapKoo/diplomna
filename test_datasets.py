import time
from tts import speak

print("=== ПРОФІЛЮВАННЯ НА РІЗНИХ НАБОРАХ ДАНИХ ===")

datasets = {
    "Малий обсяг": "Привіт.",
    "Середній обсяг": "Асистент Том і Джері готовий до роботи та чекає на ваші команди.",
    "Великий обсяг": "Голосові асистенти стали невід'ємною частиною сучасного світу. Вони допомагають швидко знаходити інформацію, керувати комп'ютером та оптимізувати щоденні рутинні завдання абсолютно без використання клавіатури чи миші."
}

for size_name, text in datasets.items():
    start_time = time.time()
    speak(text)
    duration = time.time() - start_time
    print(f"📊 {size_name} ({len(text)} символів): Час обробки = {duration:.4f} сек.")


    time.sleep(1)